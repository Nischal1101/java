
import java.util.*;

public class ForEach {

    public static void main(String args[]) {
        int[] a = {1, 2, 3, 4, 5};
        for (int i : a) {
            Scanner ins = new Scanner(System.in);
            System.out.println(ins.nextLine());
            System.out.println(i);
        }
    }
}
